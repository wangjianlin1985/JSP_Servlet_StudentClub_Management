package entity;

public class Enter
{
    private int org_id;
    private int rec_id;
    private String uid;
    private String name;
    private String sex;
    private String grade;
    private String qq;
    private String tel;
    private String mail;
    
    public int getOrg_id() {
        return this.org_id;
    }
    
    public void setOrg_id(final int org_id) {
        this.org_id = org_id;
    }
    
    public int getRec_id() {
        return this.rec_id;
    }
    
    public void setRec_id(final int rec_id) {
        this.rec_id = rec_id;
    }
    
    public String getUid() {
        return this.uid;
    }
    
    public void setUid(final String uid) {
        this.uid = uid;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(final String name) {
        this.name = name;
    }
    
    public String getSex() {
        return this.sex;
    }
    
    public void setSex(final String sex) {
        this.sex = sex;
    }
    
    public String getGrade() {
        return this.grade;
    }
    
    public void setGrade(final String grade) {
        this.grade = grade;
    }
    
    public String getQq() {
        return this.qq;
    }
    
    public void setQq(final String qq) {
        this.qq = qq;
    }
    
    public String getTel() {
        return this.tel;
    }
    
    public void setTel(final String tel) {
        this.tel = tel;
    }
    
    public String getMail() {
        return this.mail;
    }
    
    public void setMail(final String mail) {
        this.mail = mail;
    }
}
